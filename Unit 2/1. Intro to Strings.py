# Strings are words, and they're declared using quotes ""
str_one = "Hello"

# Strings can be declared using both double quotes "" and single quotes ''
str_two = 'World'

# Strings can be printed
print(str_one)

# Strings can be combined using '+'
print(str_one + str_two)

